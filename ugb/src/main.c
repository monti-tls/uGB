void* sdl_main(void* cookie)
{
    ugb_gbm* gbm = (ugb_gbm*) cookie;

    if(SDL_Init(SDL_INIT_VIDEO) < 0)
    {
        printf("SDL_Error: %s\n", SDL_GetError());
        return 0;
    }

    SDL_Window* window = SDL_CreateWindow("uGB",
        SDL_WINDOWPOS_UNDEFINED, SDL_WINDOWPOS_UNDEFINED,
        3*UGB_GPU_SCREEN_W, 3*UGB_GPU_SCREEN_H,
        SDL_WINDOW_SHOWN);

    if (!window)
    {
        printf("SDL_Error: %s\n", SDL_GetError());
        return 0;
    }

    SDL_Renderer* renderer = SDL_CreateRenderer(window, -1, 0);

    SDL_SetRenderDrawColor(renderer, 0, 0, 0, 255);
    SDL_RenderClear(renderer);
    SDL_RenderPresent(renderer);

    SDL_SetHint(SDL_HINT_RENDER_SCALE_QUALITY, "linear");
    SDL_RenderSetLogicalSize(renderer, UGB_GPU_SCREEN_W, UGB_GPU_SCREEN_H);

    SDL_Texture* tex = SDL_CreateTexture(renderer,
        SDL_PIXELFORMAT_RGB332,
        SDL_TEXTUREACCESS_STREAMING,
        UGB_GPU_SCREEN_W, UGB_GPU_SCREEN_H);

    for (;;)
    {
        SDL_UpdateTexture(tex, 0, gbm->gpu->framebuf, UGB_GPU_SCREEN_W);

        SDL_RenderClear(renderer);
        SDL_RenderCopy(renderer, tex, 0, 0);
        SDL_RenderPresent(renderer);

        SDL_Delay(100);
    }

    SDL_DestroyWindow(window);
    SDL_Quit();
    return 0;
}

int main(int argc, char** argv)
{
    ugb_gbm* gbm = ugb_gbm_create();

    /************************/
    /*** Create memory map **/
    /************************/

    ugb_mmu_map* bios = malloc(sizeof(ugb_mmu_map));
    bios->low_addr = 0x0000;
    bios->high_addr = 0x00FF;
    bios->type = UGB_MMU_RODATA;
    bios->rodata = &bios_data[0];
    ugb_mmu_add_map(gbm->mmu, bios);

    ugb_mmu_map* carthdr = malloc(sizeof(ugb_mmu_map));
    carthdr->low_addr = 0x0100;
    carthdr->high_addr = 0x014F;
    carthdr->type = UGB_MMU_DATA;
    carthdr->data = &carthdr_data[0];
    ugb_mmu_add_map(gbm->mmu, carthdr);

    ugb_mmu_map* vram = malloc(sizeof(ugb_mmu_map));
    vram->low_addr = UGB_VRAM_LOW;
    vram->high_addr = UGB_VRAM_HIGH;
    vram->type = UGB_MMU_DATA;
    vram->data = gbm->gpu->vram;
    ugb_mmu_add_map(gbm->mmu, vram);

    ugb_mmu_map* hwreg = malloc(sizeof(ugb_mmu_map));
    hwreg->low_addr = 0xFF00;
    hwreg->high_addr = 0xFF7F;
    hwreg->type = UGB_MMU_SOFT;
    hwreg->soft.handler = &ugb_hwio_mmu_handler;
    hwreg->soft.cookie = (void*) gbm->hwio;
    ugb_mmu_add_map(gbm->mmu, hwreg);

    ugb_mmu_map* zpage = malloc(sizeof(ugb_mmu_map));
    zpage->low_addr = 0xFF80;
    zpage->high_addr = 0xFFFE;
    zpage->type = UGB_MMU_DATA;
    zpage->data = &zpage_data[0];
    ugb_mmu_add_map(gbm->mmu, zpage);

    ugb_mmu_map* ie_reg = malloc(sizeof(ugb_mmu_map));
    ie_reg->low_addr = 0xFFFF;
    ie_reg->high_addr = 0xFFFF;
    ie_reg->type = UGB_MMU_SOFT;
    ie_reg->soft.handler = &ugb_cpu_ie_reg_mmu_handler;
    ie_reg->soft.cookie = gbm->cpu;
    ugb_mmu_add_map(gbm->mmu, ie_reg);

    /*****************/
    /*** Main loop ***/
    /*****************/

    pthread_t disp;
    pthread_create(&disp, 0, &sdl_main, (void*) gbm);

    int err = ugb_debugger_mainloop(gbm);
    if (err < 0)
        printf("error: %s\n", ugb_strerror(err));

    /***************/
    /*** Cleanup ***/
    /***************/

    ugb_gbm_destroy(gbm);
    return 0;
}
